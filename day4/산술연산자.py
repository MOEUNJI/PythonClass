'''
1. + : 더하기연산자
2. - : 빼기연산자
3. * : 곱하기 연산자
4. / : 나누기 연산자
5. // : 나누기 몫 연산자
6. % : 나머지 연산자
7. ** : 거듭제곱 연산자
'''

num1 = 100
num2 = 3

print(num1 + num2) # 103
print(num1 - num2) # 97
print(num1 * num2) # 300
print(num1 / num2) # 33.3333333333
print(num1 // num2) # 33(나눈 몫만 처리하기 때문에 소숫점 버림)
print(num1 % num2) # 1 (100을 3으로 나누면 나머지는 1이 됨)
print(num1 ** num2) # 100 * 100 * 100 = 10000000 ( num1을 가지고 num2숫자만큼 곱함)
